#include <stdio.h>

int main(void)
{
    // ISO C90标准下，在32位系统上，以下C表达式的结果位 false
    printf("-2147483648 < 2147483647 ? : %d\n", -2147483648 < 2147483647);  // 1


    int i = -2147483648;
    printf("i < 2147483647 ? : %d\n", i < 2147483647);  // 1


    printf("-2147483647-1 < 2147483647 ? : %d\n", -2147483647-1 < 2147483647);  // 1

    return 0;
}